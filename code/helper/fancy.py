import os
import time
from os import system, name

def reset_color():
    os.system("tput sgr0")

class bcolors:
    WARNING = '\033[93m'
    ERROR = '\033[91m'
    GOOD = '\033[92m'

def end_program():
    banner_goodbye()
    clear()
    exit()    

def clear():
    # for windows
    if name == 'nt':
        _ = system('cls')
    # for mac and linux(here, os.name is 'posix')
    else:
        _ = system('clear')

